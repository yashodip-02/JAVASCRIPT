// 23 are String Methods in JavaScript

let str = " Hello, World! ";

console.log(str.length ,'length'); //1. length
console.log(str.charAt(0),'charAt'); //2.  charAt
console.log(str.charCodeAt(1),'charCodeAt'); //3. charCodeAt
console.log(str.toUpperCase(),'toUpperCase'); //4.  toUpperCase
console.log(str.toLowerCase(),'toLowerCase'); //5. toLowerCase
console.log(str.trim(),'trim'); //6.  trim
console.log(str.slice(7, 12),'slice'); //7. slice
console.log(str.substring(0,5),'substring'); //8. substring
console.log(str.substr(7, 5),'substr'); //9. substr
console.log(str.replace("World", "JavaScript"),'replace'); //10. replace
console.log(str.replaceAll('o' , 'a'),'replaceAll'); //11. replaceAll
console.log(str.concat(" How are you?"),'concat'); // 12. concat
console.log(str.split(", "),'split'); //13. split
console.log(str.includes("World"),'includes'); //14. includes
console.log(str.indexOf("o"),'indexOf'); //15.  indexOf
console.log(str.lastIndexOf("o"),'lastIndexOf'); //16.  lastIndexOf
console.log(str.repeat(2),'repeat'); //17. repeat
console.log(str.padStart(20, "*"),'padStart'); // 18. padStart
console.log(str.padEnd(20, "*"),'padEnd'); // 19. padEnd
console.log(str.valueOf(),'valuOf');//20. valuOf
console.log(str.at(-3),'at'); //21.at
console.log(str.search("World"),'search'); //22. search
console.log(str.match('World'),'match'); //23. match
