// @4. Write a program to swap the values of two numbers without using a third (extra) variable.
//         Example:

//         Input: a = 5, b = 10

//          Output: a = 10, b = 5
// Swap means to exchange the values of two things.
 
let a = 5;
let b = 10;

a = a + b; // a =15
b = a - b; // b = 5
a = a - b; // a = 10

console.log(`after swap a = ${a} and b = ${b}`);

