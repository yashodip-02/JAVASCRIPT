/*
Positive, Negative or Zero
take a number and print 
    "Positive" if number > 0
    "Negative" if number < 0 
    "Zero" otherwise
*/

let num = 0;

if(num > 0){
    console.log(`${num} is Positive Number.`);
}

else if(num < 0) {
    console.log(`${num} is Negative Number.`);
}

else{
    console.log(`${num} is Zero`);
    
}