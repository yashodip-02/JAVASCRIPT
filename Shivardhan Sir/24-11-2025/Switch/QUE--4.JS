// Switch for season:
// 1 → Summer
// 2 → Rainy
// 3 → Winter

let season = 3;

switch (season) {
    case 1:
        console.log("Summer");
        break;

    case 2:
        console.log("Rainy");
        break;

    case 3:
        console.log("Winter");
        break;

    default:
        console.log("Unknown Season");
}