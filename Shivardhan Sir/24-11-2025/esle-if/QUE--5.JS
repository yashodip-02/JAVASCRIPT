// Marks category:
// 95 → Topper
// 70 → Good
// 50 → Average
// Else → Needs improvement

let catergory = 94;

if(catergory >= 95) {
    console.log("Topper");
}

else if(catergory >= 70) {
    console.log("Good");
}

else if(catergory >= 50) {
    console.log("Average");
}

else{
    console.log("Needs improvement");
}