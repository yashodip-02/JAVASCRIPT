// The toString() method returns a string represention of an array.

// Syntax: array.toString()

// Example: 1
let arr1 = [1, 2, 3, 4, 5];
let str1 = arr1.toString();
console.log(str1); // "1,2,3,4,5"

// Example: 2
let arr2 = ['apple', 'banana', 'cherry'];
let str2 = arr2.toString();
console.log(str2); // "apple,banana,cherry"

// Example: 3
let arr3 = [true, false, true];
let str3 = arr3.toString();
console.log(str3); // "true,false,true"

// Example: 4
let arr4 = [10, 20, 30];
let str4 = arr4.toString();
console.log(str4); // "10,20,30"

// Example: 5
let arr5 = ['a', 'b', 'c', 'd'];
let str5 = arr5.toString();
console.log(str5); // "a,b,c,d"

// Example: 6
let arr6 = [null, undefined, 0];
let str6 = arr6.toString();
console.log(str6); // ",,0"

// Example: 7
let arr7 = [1.5, 2.5, 3.5];
let str7 = arr7.toString();
console.log(str7); // "1.5,2.5,3.5"

// Example: 8
let arr8 = ['hello', 42, true]; 
let str8 = arr8.toString();
console.log(str8); // "hello,42,true"

// Example: 9
let arr9 = [];
let str9 = arr9.toString();
console.log(str9); // ""

// Example: 10
let arr10 = ['x', 'y', 'z'];
let str10 = arr10.toString();
console.log(str10); // "x,y,z"

